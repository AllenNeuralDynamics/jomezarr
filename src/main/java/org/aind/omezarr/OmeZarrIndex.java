package org.aind.omezarr;

public class OmeZarrIndex {

    private final int t;

    private final int c;

    private final int x;

    private final int y;

    private final int z;

    public OmeZarrIndex(int t, int c, int x, int y, int z) {
        this.t = t;
        this.c = c;
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getT() {
        return t;
    }

    public int getC() {
        return c;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
